function darkmode() {
    document.getElementById('dnc').style.color = "white";
    document.getElementById('time').style.color = "white";
    document.getElementById('container').style.background = "#0C181D";
}

function lightmode() {
    document.getElementById('dnc').style.color = "#0C181D";
    document.getElementById('time').style.color = "#0C181D";
    document.getElementById('container').style.background = "white";
}